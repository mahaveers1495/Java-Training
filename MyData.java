public class MyData
{
	private String MyName;
	private int MyAge;
	private int MyRoll;
	//get the details
	
	public int getAge()
	{
		return MyAge;
	}
	public int getRoll()
	{
		return MyRoll;
	}
	public String getName()
	{
		return MyName;
	}
	
	//set the details
	
	public void setAge(int newAge)
	{
		MyAge = newAge;
	}
	public void setRoll(int newRoll)
	{
		MyRoll = newRoll;
	}
	public void setName(String newName)
	{
		MyName = newName;
	}
}	
	